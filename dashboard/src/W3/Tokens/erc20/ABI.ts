export const decodeMethod = (input:string):string => {
    let sig:string = input.slice(0,10)
    let abiMethod = abiMapping[sig]
    console.log('sig',sig)

    let abiType = ABI[abiMethod]
    console.log('abi type', abiType)
    try {
        let inputs = abiType['inputs'] as inputType[]
        console.log('inputs',inputs)
        var decoded = w3.eth.abi.decodeParameters(inputs.map(v => v.type),input.slice(10))
        console.log('decoded',decoded)
    }catch (e){
        console.error(e)
        return ""
    }
    return abiType + JSON.stringify(decoded)
}

